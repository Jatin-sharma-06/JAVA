package Java;
import java.util.*;

public class inputTypes {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
//char
        System.out.println("Enter something: ");
        String text = sc.next();
        System.out.println(text);
//int
        System.out.println("Enter something: ");
        int textint = sc.nextInt();
        System.out.println(textint);
//string
        System.out.println("Enter something: ");
        String str = sc.nextLine();
        System.out.println(str);
//byte
        System.out.println("Enter something: ");
        byte textbyte = sc.nextByte();
        System.out.println(textbyte);
//float
        System.out.println("Enter something: ");
        float textfloat = sc.nextFloat();
        System.out.println(textfloat);
//double
        System.out.println("Enter something: ");
        double textDouble = sc.nextDouble();
        System.out.println(textDouble);
//boolean
        System.out.println("Enter something: ");
        boolean textBoolean = sc.nextBoolean();
        System.out.println(textBoolean);
//short
        System.out.println("Enter something: ");
        short shorttext = sc.nextShort();
        System.out.println(shorttext);
//long
        System.out.println("Enter something: ");
        long textlong = sc.nextLong();
        System.out.println(textlong);


//        System.out.println(text.length());
    }
}
